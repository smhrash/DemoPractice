package datastructurealgorithm.sorting;

public class Swap {

    public static void main(String[] args) {
        int number1=40;
        int number2=60;
        int temp;
        System.out.println("Number 1 "+number1);
        System.out.println("Number 2 "+number2);

        temp=number1;//40
        number2=temp; //40
        number1=number2;//40
        temp=number2;// 40
        System.out.println(temp);



    }
}
