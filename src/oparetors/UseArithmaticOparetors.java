package oparetors;

public class UseArithmaticOparetors {
	

	// + Additive operator
	// - subtraction
	// * Multiplication
	// / Division
	// % Remainder

	public static void main(String[] args) {

		int number1 = 90;
		int number2 = 70;

		int summation = number1 + number2;
		System.out.println("Summation value is " + summation);

		int subtraction = number1 - number2;
		System.out.println("Subtration value is " + subtraction);

		int multiply = number1 * number2;
		System.out.println("Multiplication value is " + multiply);

		double division = number1 / number2;
		System.out.println("Division value is " + division);

		double remainder = number1 % number2;
		System.out.println("Remiander value is " + remainder);

		// 7/2= division Value 3 reminder 1

	}

}


